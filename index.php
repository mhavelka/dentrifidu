              <h2 class="fancy">CO ČEKAT</h2>
            </div>
            <div class="col-sm-6 col-sm-offset-3">
              <br><br>
            </div>
            <div class="col-sm-8 col-sm-offset-2">
              <br><strong>Obecně</strong><br><br>
              <p>Den trifidů je larp inspirovaný stejnojmenným románem a odehrávající se v roce 1955 na předměstí Londýna a v jeho okolí. Historie v naší hře se odehrávala tak, jak ji známe (včetně první a druhé světové války) s malými odchylkami po roce 1945. Důležitými změnami jsou mírné odchylky ve společenském uspořádání (probíhá Studená válka, nicméně o SSSR neví běžný západní člověk téměř nic, existují pokročilé sledovací i zbraňové satelitní technologie, ve společnosti mají ženy o něco lepší postavení, než tomu bylo ve skutečnosti). Důležitou změnou jsou trifidi - vysoké rostliny jejichž olej se rychle stává jednou z nejcennějších surovin světa. Rostliny, které se ale zároveň dokáží pomalu pohybovat i útočit dlouhým a prudce jedovatým šlahounem.
              </p>

              <br><strong>Prakticky</strong><br><br>
              <p>Každý hráč se stává členem skupiny lidí, kteří všichni více či méně pocházejí z londýnské čtvrti Highgate. Několik dnů před začátkem hry došlo k obrovské katastrofě -způsobené zářícím předmětem na obloze připomínajícím kometu. Ta přes jednu jedinou noc připravila o zrak drtivou většinu lidí (přinejmenším v Londýně, jak je to jinde, nikdo zatím neví). Události uplynulých dní pak vedly k tomu, že se jednotlivé postavy ocitají na jednom specifickém místě, na kterém také začíná hra.
              </p>
              <br><strong>Žánrově</strong><br><br>
              <p>Žánrově jde o vážné drama s hororovými prvky z prostředí alternativní historie. Hra je silně strukturovaná a má jasně daný příběh s několika konci, ke kterým se lze (na základě rozhodování během hry) dobrat. Klade důraz na skupinovou hru a společný příběh, na snahu zachránit si život i na filosofická témata o budování nové společnosti. Součástí hry budou také  individuální vztahy a cíle. Jejich budování či rozehrávání bude ovšem ovlivněno klíčovým prvkem:  Většina hráčů bude po celou dobu hry hrát bez možnosti použít zrak (obdrží neprůhlednou masku přes oči). Hra tak bude především o pohybu bez zraku, zvucích a hudbě, fyzickém kontaktu či o vůni a pachu.
              <br>
              <br><strong>CO VÁS ČEKÁ:</strong> Rychlé přesuny poslepu, skupinový příběh, napětí, vztahy, rozhodování
              <br><strong>CO VÁS NEČEKÁ:</strong> Boj replikami zbraní, volný svět s neomezenými možnostmi
              </p>
              <br><strong>Proč?</strong><br><br>
              <p>Protože to chceme zkusit.<br><br><strong>Víme, že to není hra pro všechny, ale věříme, že skrze ni můžeme prozkoumat nové možnosti</strong>, které larp nabízí.
              Zažít něco, díky čemu bude možné nahlížet na některé věci zase trochu jinak.<br><br>Neděláme zážitkovou hru, děláme larp.</p>
