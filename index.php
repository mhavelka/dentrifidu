              <p> Londýnské předměstí Highgate je v roce 1955 zeleným přílepkem metropole s pár tisíci obyvateli, kteří žijí v řadových i rodinných domech propojených čerstvě položeným asfaltem. Do centra chodí obyvatelé Highgate pěšky nebo jezdí vlakem či autobusem, který jezdí pětkrát denně.  Během války dopadlo na Highgate omylem několik bomb z nacistických bombardérů, což život ve čtvrti výrazným způsobem poznamenalo.
              <br>Asi 20 kilometrů od Highgate, u Farnham se nachází jedna z největších londýnských pěstíren trifidů. Ti jsou zde zpracováváni a odesíláni na centrální linku, kde je z nich vyráběn olej.

              <p>V úterý 13. září 1955 se to stalo. Na obloze se objevila kometa, kterou si nechal ujít jen málokdo.  Ve středu 14. září se většina populace probudila slepá. Společnost se začala hroutit, zrak si uchovalo totiž jen pár procent lidí. Rychle se začalo rabovat, lidé umírali, společenské struktury se hroutily. Na předměstí Highgate se tři dny po události (16. září) objevila první skupina lidí s zbraněmi v rukách. Vykradli to, co zbylo v obchodech, a odešli. Dalšího dne přišel jiný gang, jenže ten už se zaměřil na lidi. Jeho členové podle svého uvážení zabíjeli nebo brali nevidomé jako novodobé otroky. Mezitím se nedaleko probouzí trifidi, kteří vycítili příležitost…
              </p>
              <p>Středa 21. září. Skupina lidí z Highgate se ocitá na neznámém místě. Již tři dny je zde jejich věznitelé učí konkrétním pracím, které mají vykonávat. Nevidomí vyrábí zbraně a nástroje, vaří, perou a slouží jako sexuální otroci či jako cvičné terče, na kterých si otrokáři vybíjejí vztek. Jenže i mezi nimi se najdou ti, kterým se to nelíbí. Ti, kteří nechtějí navěky shnít v Londýně, nechtějí po zbytek života jen sloužit. Věří v záchranu, v to, že někde ještě společnost funguje. Ale opravdu tomu tak je? A především: Mají ve své slepotě vůbec šanci se svým osudem něco udělat?
              </p>
