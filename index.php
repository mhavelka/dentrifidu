O HŘE
Pýcha předchází pád. A pýcha lidstva zhýčkaného vlastní nadvládou nad planetou dosáhla samého vrcholu. Až do toho dne. Zdrcení lidé to nazývají apokalypsou, fanatici zaslouženým trestem a věční optimisté novým začátkem.  Stačilo ztratit zrak, aby se z lovců stala kořist. Začal první Den trifidů. 
Larp Den trifidů bude hra inspirovaná stejnojmenným románem britského spisovatele Johna Wyndhama. Potrvá kolem osmi hodin a bude zcela jiná než hry, které hrajete. Žánrově půjde o postapokalyptické drama s hororovými prvky.   Důraz bude kladen na silné postavy a příběh. To vše předávané prostřednictvím sluchu, čichu, chuti a hmatu. Ano, jak již tušíte, většina postav ve Dni trifidů bude slepá. Naší snahou je vytvořit plnohodnotnou hru založenou na jiných než vizuálních vjemech, k čemuž pomůže i důkladná předherní příprava. 

PROLOG




Září 1956, Londýn, sedm let od skončení druhé světové války. Svět se vzpamatoval rychle. Možná až příliš rychle. Místo tolik potřebného míru je totiž na spadnutí další konflikt. Tentokrát mezi západní liberální a dynamicky se rozvíjející společností a východním blokem, neznámým uzavřeným světem tvořeným hlavně SSSR a Čínou. Rovnováha je udržována hlavně pomocí velkého počtu sledovacích satelitů a ničivých zbraní namířených na všechna velká města světa. Dobývání vesmíru odstartovalo krátce po válce. V roce 1949 byl vynesen první satelit na oběžnou dráhu, dnes jich tam jsou údajně desítky. 
Londýnské předměstí Highgate je v roce 1955 zeleným přílepkem metropole s pár tisíci obyvateli, kteří žijí v řadových i rodinných domech propojených čerstvě položeným asfaltem. Do centra chodí obyvatelé Highgate pěšky nebo jezdí vlakem či autobusem, který jezdí pětkrát denně. Během války dopadlo na Highgate omylem několik bomb z nacistických bombardérů, což život ve čtvrti výrazným způsobem poznamenalo.  Asi 20 kilometrů od Highgate, u Farnham se nachází jedna z největších londýnských pěstíren trifidů. Ti jsou zde zpracováváni a odesíláni na centrální linku, kde je z nich vyráběn olej. 
V úterý 13. září 1955 se to stalo. Na obloze se objevila kometa, kterou si nechal ujít jen málokdo. Ve středu 14. září se většina populace probudila slepá. Společnost se začala hroutit, zrak si uchovalo totiž jen pár procent lidí. Rychle se začalo rabovat, lidé umírali, společenské struktury se hroutily. Na předměstí Highgate se tři dny po události (16. září) objevila první skupina lidí s zbraněmi v rukách. Vykradli to, co zbylo v obchodech, a odešli. Dalšího dne přišel jiný gang, jenže ten už se zaměřil na lidi. Jeho členové podle svého uvážení zabíjeli nebo brali nevidomé jako novodobé otroky. Mezitím se nedaleko probouzí trifidi, kteří vycítili příležitost… 
Středa 21. září. Skupina lidí z Highgate se ocitá na neznámém místě. Již tři dny je zde jejich věznitelé učí konkrétním pracím, které mají vykonávat. Nevidomí vyrábí zbraně a nástroje, vaří, perou a slouží jako sexuální otroci či jako cvičné terče, na kterých si otrokáři vybíjejí vztek. Jenže i mezi nimi se najdou ti, kterým se to nelíbí. Ti, kteří nechtějí navěky shnít v Londýně, nechtějí po zbytek života jen sloužit. Věří v záchranu, v to, že někde ještě společnost funguje. Ale opravdu tomu tak je? A především: Mají ve své slepotě vůbec šanci se svým osudem něco udělat? 
CO ČEKAT



Obecně

Den trifidů je larp inspirovaný stejnojmenným románem a odehrávající se v roce 1955 na předměstí Londýna a v jeho okolí. Historie v naší hře se odehrávala tak, jak ji známe (včetně první a druhé světové války) s malými odchylkami po roce 1945. Důležitými změnami jsou mírné odchylky ve společenském uspořádání (probíhá Studená válka, nicméně o SSSR neví běžný západní člověk téměř nic, existují pokročilé sledovací i zbraňové satelitní technologie, ve společnosti mají ženy o něco lepší postavení, než tomu bylo ve skutečnosti). Důležitou změnou jsou trifidi - vysoké rostliny jejichž olej se rychle stává jednou z nejcennějších surovin světa. Rostliny, které se ale zároveň dokáží pomalu pohybovat i útočit dlouhým a prudce jedovatým šlahounem. 

Prakticky

Každý hráč se stává členem skupiny lidí, kteří všichni více či méně pocházejí z londýnské čtvrti Highgate. Několik dnů před začátkem hry došlo k obrovské katastrofě -způsobené zářícím předmětem na obloze připomínajícím kometu. Ta přes jednu jedinou noc připravila o zrak drtivou většinu lidí (přinejmenším v Londýně, jak je to jinde, nikdo zatím neví). Události uplynulých dní pak vedly k tomu, že se jednotlivé postavy ocitají na jednom specifickém místě, na kterém také začíná hra. 

Žánrově

Žánrově jde o vážné drama s hororovými prvky z prostředí alternativní historie. Hra je silně strukturovaná a má jasně daný příběh s několika konci, ke kterým se lze (na základě rozhodování během hry) dobrat. Klade důraz na skupinovou hru a společný příběh, na snahu zachránit si život i na filosofická témata o budování nové společnosti. Součástí hry budou také individuální vztahy a cíle. Jejich budování či rozehrávání bude ovšem ovlivněno klíčovým prvkem: Většina hráčů bude po celou dobu hry hrát bez možnosti použít zrak (obdrží neprůhlednou masku přes oči). Hra tak bude především o pohybu bez zraku, zvucích a hudbě, fyzickém kontaktu či o vůni a pachu.   CO VÁS ČEKÁ: Rychlé přesuny poslepu, skupinový příběh, napětí, vztahy, rozhodování  CO VÁS NEČEKÁ: Boj replikami zbraní, volný svět s neomezenými možnostmi 

Proč?

Protože to chceme zkusit.  Víme, že to není hra pro všechny, ale věříme, že skrze ni můžeme prozkoumat nové možnosti, které larp nabízí. Zažít něco, díky čemu bude možné nahlížet na některé věci zase trochu jinak.  Neděláme zážitkovou hru, děláme larp.
